package com.company;

public class Main {

    public static void main(String[] args) {

        for (int y = 1; y < 12; y++) {
            if (y == 1) {
                System.out.print("     ___     ");
            } else if (y == 2) {
                System.out.print("    /  /\\    ");
            } else if (y == 3) {
                System.out.print("   /  /::\\   ");
            } else if (y == 4) {
                System.out.print("  /  /:/\\:\\  ");
            } else if (y == 5) {
                System.out.print(" /  /:/ /::\\ ");
            } else if (y == 6) {
                System.out.print("/__/:/ /:/\\:\\");
            } else if (y == 7) {
                System.out.print("\\  \\:\\/:/  \\/");
            } else if (y == 8) {
                System.out.print(" \\  \\::/     ");
            } else if (y == 9) {
                System.out.print("  \\  \\:\\     ");
            } else if (y == 10) {
                System.out.print("   \\  \\:\\    ");
            } else if (y == 11) {
                System.out.print("    \\__\\/    ");
            }
            System.out.println();
        }
    }
}

